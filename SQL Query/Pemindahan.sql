SELECT 
	transferstok.`KodeTF` AS transferstok_KodeTF,
	transferstok.`KodeUser` AS transferstok_KodeUser,
	transferstok.`Tanggal` AS transferstok_Tanggal,
	transferstok.`KodeKerja` AS transferstok_KodeKerja,
	transferstok.`KodeMobil` AS transferstok_KodeMobil,
	transferstok.`OutletAsal` AS transferstok_OutletAsal,
	transferstok.`OutletTujuan` AS transferstok_OutletTujuan,     
	transferstok.`StatusDelivery` AS transferstok_StatusDelivery,
	transferstok.`Biaya` AS transferstok_Biaya,
	transferstok.`Keterangan` AS transferstok_Keterangan,
    user.`KodeUser` AS user_KodeUser,
    user.`NoIdentitas` AS user_NoIdentitas,
    user.`KodeOutlet` AS user_KodeOutlet,
    user.`Username` AS user_Username,
    user.`Password` AS user_Password,
    IdUser.`NoIdentitas` AS identitas_NoIdentitas,
	IdUser.`KodeJenis` AS identitas_KodeJenis,
	IdUser.`NamaLengkap` AS identitas_NamaLengkap,
	IdUser.`JenisKelamin` AS identitas_JenisKelamin,
	IdUser.`TempatLahir` AS identitas_TempatLahir,
	IdUser.`TglLahir` AS identitas_TglLahir,
	IdUser.`Alamat` AS identitas_Alamat,
	IdUser.`RT` AS identitas_RT,
	IdUser.`RW` AS identitas_RW,
	IdUser.`Kelurahan` AS identitas_Kelurahan,
	IdUser.`Kecamatan` AS identitas_Kecamatan,
	IdUser.`Kabupaten` AS identitas_Kabupaten,
	IdUser.`Agama` AS identitas_Agama,
	IdUser.`StatusPernikahan` AS identitas_StatusPernikahan,
	IdUser.`Pekerjaan` AS identitas_Pekerjaan,
	IdUser.`Kewarganegaraan` AS identitas_Kewarganegaraan,
    pekerjaan.`KodeKerja` AS pekerjaan_KodeKerja,
	pekerjaan.`NoIdentitas` AS pekerjaan_NoIdentitas,
	pekerjaan.`KodeJob` AS pekerjaan_KodeJob,
	pekerjaan.`KodeOutlet` AS pekerjaan_KodeOutlet,
	pekerjaan.`JenisPembayaran` AS pekerjaan_JenisPembayaran,
	pekerjaan.`Gaji` AS pekerjaan_Gaji,
    IdDriver.`NoIdentitas` AS identitas_NoIdentitas,
    IdDriver.`KodeJenis` AS identitas_KodeJenis,
    IdDriver.`NamaLengkap` AS identitas_NamaLengkap,
    IdDriver.`JenisKelamin` AS identitas_JenisKelamin,
    IdDriver.`TempatLahir` AS identitas_TempatLahir,
    IdDriver.`TglLahir` AS identitas_TglLahir,
    IdDriver.`Alamat` AS identitas_Alamat,
    IdDriver.`RT` AS identitas_RT,
    IdDriver.`RW` AS identitas_RW,
    IdDriver.`Kelurahan` AS identitas_Kelurahan,
    IdDriver.`Kecamatan` AS identitas_Kecamatan,
    IdDriver.`Kabupaten` AS identitas_Kabupaten,
    IdDriver.`Agama` AS identitas_Agama,
    IdDriver.`StatusPernikahan` AS identitas_StatusPernikahan,
    IdDriver.`Pekerjaan` AS identitas_Pekerjaan,
    IdDriver.`Kewarganegaraan` AS identitas_Kewarganegaraan,
    OutletAsal.`KodeOutlet` AS outlet_KodeOutlet,
	OutletAsal.`NamaOutlet` AS outlet_NamaOutlet,
	OutletAsal.`Alamat` AS outlet_Alamat,
	OutletAsal.`Kelurahan` AS outlet_Kelurahan,
	OutletAsal.`Kecamatan` AS outlet_Kecamatan,
	OutletAsal.`Kota` AS outlet_Kota,
	OutletAsal.`Provinsi` AS outlet_Provinsi,
    OutletTujuan.`KodeOutlet` AS outlet_KodeOutlet,
	OutletTujuan.`NamaOutlet` AS outlet_NamaOutlet,
	OutletTujuan.`Alamat` AS outlet_Alamat,
	OutletTujuan.`Kelurahan` AS outlet_Kelurahan,
	OutletTujuan.`Kecamatan` AS outlet_Kecamatan,
	OutletTujuan.`Kota` AS outlet_Kota,
	OutletTujuan.`Provinsi` AS outlet_Provinsi,
    rinciantransfer.`KodeRincian` AS rinciantransfer_KodeRincian,
	rinciantransfer.`KodeTF` AS rinciantransfer_KodeTF,
	rinciantransfer.`Barcode` AS rinciantransfer_Barcode,
	rinciantransfer.`Satuan` AS rinciantransfer_Satuan,
	rinciantransfer.`Kuantitas` AS rinciantransfer_Kuantitas,
	rinciantransfer.`HargaModal` AS rinciantransfer_HargaModal,
	rinciantransfer.`TotalModal` AS rinciantransfer_TotalModal 
    FROM transferstok transferstok 
    INNER JOIN user user INNER JOIN identitas IdUser INNER JOIN pekerjaan pekerjaan INNER JOIN identitas IdDriver 
    INNER JOIN outlet OutletAsal INNER JOIN outlet OutletTujuan INNER JOIN rinciantransfer rinciantransfer ON  
    (transferstok.KodeUser=user.KodeUser AND IdUser.NoIdentitas=user.NoIdentitas AND transferstok.KodeKerja=pekerjaan.KodeKerja 
    AND pekerjaan.NoIdentitas=IdDriver.NoIdentitas AND transferstok.OutletAsal=OutletAsal.KodeOutlet AND 
    transferstok.OutletTujuan=OutletTujuan.KodeOutlet AND transferstok.KodeTF=rinciantransfer.KodeTF);
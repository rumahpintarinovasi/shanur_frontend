    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if(txtNamaBisnis.getText().trim().length()==0){
            JOptionPane.showMessageDialog(this, "Nama Bisnis Belum Diisi");
            return;
        }
        
        String NamaBisnis = (((String)txtNamaBisnis.getText().trim()).toString());
        Bisnis.setNamBisnis(NamaBisnis);
        
        try {
            Bisnis.Simpan(NamaBisnis);
            DBConnector.getConnection().commit();
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(this, "Nama Bisnis Berhasil Disimpan");
        } 
        catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error Saat Menyimpan Nama Bisnis :"+e.getMessage(),
                    "Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_btnSimpanActionPerformed


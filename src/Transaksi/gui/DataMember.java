    private void btnTampilkanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTampilkanActionPerformed
        // TODO add your handling code here:
        if(rbNamaMember.isSelected()){
            String NamaMember = (((String)txtNamaMember.getText().trim()).toString());
            mbr.getMember2(tblMember, NamaMember);
        }
        else if(rbLevel.isSelected()){
            String Level = (((String)cboLevel.getSelectedItem()).toString());
            mbr.getMember7(tblMember, Level);
        }
        else if(rbTglExpire.isSelected()){
            Calendar TglExpire = dchTglExpire.getCalendar();
            mbr.getMember5(tblMember, TglExpire);
        }
        else if(rbBulan.isSelected()){
            int Bulan = mchBulan.getMonth()+1;
            int Tahun = ychTahun.getYear();
            mbr.getMember6(tblMember, Bulan, Tahun);
        }
        else if(rbSemua.isSelected()){
            mbr.getMember(tblMember);
        }
        
        int Left = SwingConstants.LEFT;
        int Center = SwingConstants.CENTER;
        int Right = SwingConstants.RIGHT;
        
        Tab.TableAlignment10(tblMember, Center, Center, Left, Left, Left, Left, Left, Center, Center, Center, Center);
    }//GEN-LAST:event_btnTampilkanActionPerformed


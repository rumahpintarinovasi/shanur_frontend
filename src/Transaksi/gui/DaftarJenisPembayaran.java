    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        EditJenisPembayaran EJP = new EditJenisPembayaran(this, true);
        
        DefaultTableModel dtm = (DefaultTableModel)tblJenisPembayaran.getModel();
        int row = tblJenisPembayaran.getSelectedRow();
        if(row <= -1){
            JOptionPane.showMessageDialog(this, "Jenis Pembayaran Belum Dipilih");
            return;
        }
        int KodeJenis = (((Integer)dtm.getValueAt(row, 0)).intValue());
        EJP.ftxtKodeJenis.setValue(new Integer(KodeJenis));
        String NamaJenis = JP.getNamaJenis(KodeJenis);
        EJP.txtJenisPembayaran.setText(new String(NamaJenis));
        
        EJP.setLocationRelativeTo(this);
        EJP.setVisible(true);
        
        if(txtJenisPembayaran.getText().trim().length()>0){
            String JenisPembayaran = (((String)txtJenisPembayaran.getText().trim()).toString());
            JP.getJenisPembayaran(tblJenisPembayaran, JenisPembayaran);
        }
        else if(txtJenisPembayaran.getText().trim().length()==0){
            JP.getJenisPembayaran(tblJenisPembayaran);
        }
    }//GEN-LAST:event_btnEditActionPerformed


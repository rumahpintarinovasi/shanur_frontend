    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if(txtJenisPekerjaan.getText().trim().length()==0){
            JOptionPane.showMessageDialog(this, "Jenis Pekerjaan Belum Diisi");
            return;
        }
        
        int KodeJenis = (((Integer)ftxtKodeJenis.getValue()).intValue());
        String JenisPekerjaan = (((String)txtJenisPekerjaan.getText().trim()).toString());
        
        try {
            JP.UpdateNamaPekerjaan(KodeJenis, JenisPekerjaan);
            
            DBConnector.getConnection().commit();
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(this, "Data Berhasil Diperbarui");
        } 
        catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error Saat Memperbarui Data : "+e.getMessage(),
                    "Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        setVisible(false);
        dispose();
    }//GEN-LAST:event_btnSimpanActionPerformed


    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if(txtNamaJenis.getText().trim().length()==0){
            JOptionPane.showMessageDialog(this, "Nama Jenis Belum Diisi");
            return;
        }
        
        String NamaJenis = (((String)txtNamaJenis.getText().trim()).toString());
        
        try {
            JI.Simpan(NamaJenis);
            
            DBConnector.getConnection().commit();
            Toolkit.getDefaultToolkit().beep();
            txtNamaJenis.setText(null);
            JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan");
        } 
        catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error Saat Menyimpan Jenis Identitas : "+e.getMessage(),
                    "Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_btnSimpanActionPerformed


    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if(txtWaktuMulai.getText().trim().length()==0){
            JOptionPane.showMessageDialog(this, "Waktu Mulai Belum Diisi");
            return;
        }
        if(txtWaktuSelesai.getText().trim().length()==0){
            JOptionPane.showMessageDialog(this, "Waktu Selesau Belum Diisi");
            return;
        }
        
        int Sift = (((Integer)ftxtSift.getValue()).intValue());
        String WaktuMulai = (((String)txtWaktuMulai.getText().trim()).toString());
        String WaktuSelesau = (((String)txtWaktuSelesai.getText().trim()).toString());
        
        try {
            Sft.UpdateStartTime(Sift, WaktuMulai);
            Sft.UpdateFinishTime(Sift, WaktuSelesau);
            
            DBConnector.getConnection().commit();
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(this, "Data Berhasil Diperbarui");
            
            setVisible(false);
            dispose();
        } 
        catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error Saat Memperbarui Sift : "+e.getMessage(),
                    "Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_btnSimpanActionPerformed


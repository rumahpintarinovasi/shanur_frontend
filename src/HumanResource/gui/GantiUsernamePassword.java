    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:

        int KodeUser = (((Integer)ftxtKodeUser.getValue()).intValue());
        
        try {
            if(txtUsername.getText().trim().length()>0){
                String Username = (((String)txtUsername.getText().trim()).toString());
                Usr.UpdateUsername(KodeUser, Username);
            }
            if(txtPassword.getText().trim().length()>0){
                String Password = (((String)txtPassword.getText().trim()).toString());
                Usr.UpdatePasswordSHA2(KodeUser, Password);
            }
            
            DBConnector.getConnection().commit();
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(this, "Data Berhasil Diperbarui");
        } 
        catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error Saat Memperbarui Data : "+e.getMessage(),
                    "Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_btnUpdateActionPerformed


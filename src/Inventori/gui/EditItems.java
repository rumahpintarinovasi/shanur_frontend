    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if(txtBarcode.getText().trim().length()==0){
            JOptionPane.showMessageDialog(this, "Barcode Harus Diisi");
            return;
        }
        if(txtNamaItem.getText().trim().length()==0){
            JOptionPane.showMessageDialog(this, "Nama Item Harus Diisi");
            return;
        }

        String Barcode = (((String)txtBarcode.getText().trim()).toString());
        Item.setBarcode(Barcode);
        String KodeBarcode = (((String)txtKodeBarcode.getText().trim()).toString());
        String NamaItem = (((String)txtNamaItem.getText().trim()).toString());
        Item.setNamaItem(NamaItem);
        int KodeKategori = (((Integer)ftxtKodeKategori.getValue()).intValue());
        Item.setKodeKategori(KodeKategori);
        int KodeMerek = (((Integer)ftxtKodeMerek.getValue()).intValue());
        Item.setKodeMerek(KodeMerek);

        try {
            Item.UpdateNamaItem(Barcode, NamaItem);
            if(cboJenisItem.getSelectedIndex()==1){
                Item.UpdateJenisItem(Barcode, 1);
            }
            else if(cboJenisItem.getSelectedIndex()==2){
                Item.UpdateJenisItem(Barcode, 2);
            }

            if(cboKategori.getSelectedIndex()>0){
                Item.UpdateKodeKategori(Barcode, KodeKategori);
            }

            if(cboMerek.getSelectedIndex()>0){
                Item.UpdateKodeMerek(Barcode, KodeMerek);
            }

//            Item.UpdateBarcode(KodeBarcode, Barcode);

            DBConnector.getConnection().commit();
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(this, "Data Berhasil Diperbarui");
        }
        catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error Saat Memperbarui Data : "+e.getMessage(),
                "Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_btnSimpanActionPerformed


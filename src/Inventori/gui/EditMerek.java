    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if(txtNamaMerek.getText().trim().length()==0){
            JOptionPane.showMessageDialog(this, "Nama Merek Belum Diisi");
            return;
        }
        
        int KodeMerek = (((Integer)ftxtKodeMerek.getValue()).intValue());
        String NamaMerek = (((String)txtNamaMerek.getText().trim()).toString());
        
        try {
            Mrk.UpdateNamaMerek(KodeMerek, NamaMerek);
            
            DBConnector.getConnection().commit();
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan");
            
            setVisible(false);
            dispose();
        } 
        catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error Saat Memberparui Merek : "+e.getMessage(),
                    "Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_btnSimpanActionPerformed


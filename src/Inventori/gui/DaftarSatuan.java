    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        EditSatuan ES = new EditSatuan();
        
        int row = tblSatuan.getSelectedRow();
        DefaultTableModel dtm = (DefaultTableModel)tblSatuan.getModel();
        
        int KodeSatuan = (((Integer)dtm.getValueAt(row, 0)).intValue());
        String NamaSatuan = (((String)dtm.getValueAt(row, 1)).toString());
        
        ES.ftxtKodeSatuan.setValue(new Integer(KodeSatuan));
        ES.txtNamaSatuan.setText(new String(NamaSatuan));
        
        ES.setLocationRelativeTo(this);
        ES.setVisible(true);
        
        if(txtNamaSatuan.getText().trim().length()==0){
            Sat.getSatuan(tblSatuan);
        }
        else if(txtNamaSatuan.getText().trim().length()>0){
            String NmSatuan = (((String)txtNamaSatuan.getText().trim()).toString());
            Sat.getSatuan(tblSatuan, NmSatuan);
        }
    }//GEN-LAST:event_btnEditActionPerformed


    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if(txtNamaKategori.getText().trim().length()==0){
            JOptionPane.showMessageDialog(this, "Nama Kategori Belum Diisi");
            return;
        }

        String NamaKategori = (((String)txtNamaKategori.getText().trim()).toString());

        try {
            Kategori.Simpan(NamaKategori);
            DBConnector.getConnection().commit();
            
            Toolkit.getDefaultToolkit().beep();
            txtNamaKategori.setText(null);
            JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan");
        }
        catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error Saat Registrasi Kategori : "+e.getMessage(),
                "Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_btnSimpanActionPerformed


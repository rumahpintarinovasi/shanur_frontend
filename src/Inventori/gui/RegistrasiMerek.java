    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if(txtNamaMerek.getText().trim().length()==0){
            JOptionPane.showMessageDialog(this, "Nama Merek Belum Diisi");
            return;
        }
        
        String NamaMerek = (((String)txtNamaMerek.getText().trim()).toString());
        
        try {
            Merek.Simpan(NamaMerek);
            
            DBConnector.getConnection().commit();
            Toolkit.getDefaultToolkit().beep();
            txtNamaMerek.setText(null);
            JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan");
        } 
        catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error Saat Menyimpan Merek : "+e.getMessage(),
                    "Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_btnSimpanActionPerformed


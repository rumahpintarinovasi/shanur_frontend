    private void btnTampilkanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTampilkanActionPerformed
        // TODO add your handling code here:
        if(rbTanggal.isSelected()){
            Calendar TglAwal = dchTglAwal.getCalendar();
            Calendar TglAkhir = dchTglAkhir.getCalendar();
            
            SB.getStokBarang(tblHistoriStokItem, TglAwal, TglAkhir);
        }
        else if(rbBulan.isSelected()){
            int Bulan = mchBulan.getMonth()+1;
            int Tahun = ychTahun.getYear();
            
            SB.getStokBarang(tblHistoriStokItem, Bulan, Tahun);
        }
    }//GEN-LAST:event_btnTampilkanActionPerformed

